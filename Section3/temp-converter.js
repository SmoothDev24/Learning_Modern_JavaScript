let fahrenheit = 50;
console.log("The temperature in fahrenheit is: " + fahrenheit + 'F.')

let celsius = ((fahrenheit - 32)* (5/9));
console.log('The fahrenheit temperature converted to Celsius is: ' + celsius + 'C.')

let kelvin = (celsius + 273.15);
console.log('The fahrenheit temperature converted to Kelvin is: ' + kelvin + 'K.')
